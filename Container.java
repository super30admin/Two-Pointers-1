/*
    We iterate through the heights and calculate the area of the rectangle.
    We take min of the left ad right heights and return the maximum area.
*/
class Solution {
    public int maxArea(int[] height) {
         int maxarea = 0, l = 0, r = height.length - 1;
        while (l < r) {
            maxarea = Math.max(maxarea, Math.min(height[l], height[r]) * (r - l));
            if (height[l] < height[r])
                l++;
            else
                r--;
        }
        return maxarea;
        
    }
}